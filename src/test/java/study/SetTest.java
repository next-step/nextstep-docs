package study;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.params.ParameterizedTest;
import org.junit.jupiter.params.provider.CsvSource;
import org.junit.jupiter.params.provider.ValueSource;

import static org.assertj.core.api.Assertions.assertThat;
import static org.junit.jupiter.api.Assertions.*;
import java.util.HashSet;
import java.util.Set;

public class SetTest {
    private Set<Integer> numbers;

    @BeforeEach
    void setUp(){
        numbers = new HashSet<>();
        numbers.add(1);
        numbers.add(1);
        numbers.add(2);
        numbers.add(3);
    }

    @Test
    @DisplayName("Set 크기 확인")
    void setSize(){
        int actual=numbers.size();
        System.out.println("Set 크기 Test");
        assertEquals(3,actual);
    }

    @ParameterizedTest
    @ValueSource(ints={1,2,3})
    void contains(int element) {
        System.out.println(numbers);
        assertThat(numbers.contains(element)).isTrue();
    }
//
    @ParameterizedTest
    @CsvSource(value = {"1:true", "2:true", "3:true", "4:false", "5:false"}, delimiter = ':')
    void toLowerCase_ShouldGenerateTheExpectedLowercaseValue(int input,String expected) {
        String actual=Boolean.toString(numbers.contains(input));
        assertEquals(expected,actual);
    }

}
